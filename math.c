#include <stdio.h>

int main() {
  int a = 15;
  int b = 20;

  int sum = a + b;
  int difference = a - b;
  int product = a * b;
  float ratio = a / b;

  printf("First number: %d\n", a);
  printf("Second number: %d\n", b);
  printf("Sum: %d\n", sum);
  printf("Difference: %d\n", difference);
  printf("Product: %d\n", product);
  printf("Ratio: %f\n", ratio);
}
